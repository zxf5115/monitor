import sys

class Options:

  # -----------------------------------------------------------------------
  # 初始化方法

  def __init__(self):

    self.options = {}




  # -----------------------------------------------------------------------
  # 添加选项

  def add(self, flag, callback):

    self.options[flag] = callback




  # -----------------------------------------------------------------------
  # 初始化方法，用于数据初始化

  def execute(self):

    if len(sys.argv) > 2:

      self.options[sys.argv[1]](sys.argv[2])

    else:

      self.options[sys.argv[1]]()
